package com;

public class Bags {
	
	private String bagNumber;

	private String bagEntryPoint;

	private String bagFlightID;

	public String getBagNumber() {
		return bagNumber;
	}

	public void setBagNumber(String bagNumber) {
		this.bagNumber = bagNumber;
	}

	public String getBagEntryPoint() {
		return bagEntryPoint;
	}

	public void setBagEntryPoint(String bagEntryPoint) {
		this.bagEntryPoint = bagEntryPoint;
	}

	public String getBagFlightID() {
		return bagFlightID;
	}

	public void setBagFlightID(String bagFlightID) {
		this.bagFlightID = bagFlightID;
	}


}
